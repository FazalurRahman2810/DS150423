# * 
# /  - always returns a float value
# %  - Modulus (Mod)  - returns remainder
# // - Floor Division - convert that float and return int   3.9 -> 3
# ** - Exponential    - power of , raise to

no1 = 10
no2 = 5

add = no1 + no2
print("Addition : ",add)

sub = no1 - no2
print("Subtraction : ",sub)

multi = no1*no2
print("Multiplication : ",multi)

div=no1/no2
print("Division : ",div)

mod=no1%no2
print("Modulus : ",mod)

floordiv=no1//no2
print("Floor Division : ",floordiv)

pow=no1**no2
print("Power : ",pow)

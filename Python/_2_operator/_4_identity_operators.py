# Identity Operator
# is     - returns True if both the variables are referring to the same object
# is not - returns False if both the variables are referring to the same object

no1 = 10
no2 = 10

is_demo = no1 is no2
print("Is : ",is_demo)

is_not_demo = no1 is not no2
print("Not is : ",is_not_demo)

